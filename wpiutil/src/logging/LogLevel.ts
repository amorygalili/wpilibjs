/**
 * Log levels for the WPILib logger
 */
export enum LogLevel {
  /**
   * Critical error messages
   */
  Critical = 50,
  
  /**
   * Error messages
   */
  Error = 40,
  
  /**
   * Warning messages
   */
  Warning = 30,
  
  /**
   * Informational messages
   */
  Info = 20,
  
  /**
   * Debug messages
   */
  Debug = 10,
  
  /**
   * Trace messages (most verbose)
   */
  Trace = 0
}
